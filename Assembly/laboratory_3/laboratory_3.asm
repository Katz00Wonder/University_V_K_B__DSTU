
    array db 0x01, 0x00, 0x01, 0x00, 0x00, 0x01, 0x00  ; Пример массива
    len equ $ - array                                 ; Длина массива

section .text
global _start
_start:
    mov ebx, array                                    ; Загрузка адреса массива в регистр EBX
    xor ecx, ecx                                       ; Инициализация счетчика нулей
    xor edx, edx                                      ; Инициализация индекса
    jmp loop                                          ; Переход к циклу обработки массива

loop:
    movzx eax, byte [ebx + edx]                        ; Загрузка байта из массива в регистр EAX
    test eax, 1                                        ; Проверка старшего бита
    jz skip                                          ; Если ноль, переход к следующему байту
    inc edx                                          ; Увеличение индекса
    cmp edx, len                                      ; Сравнение с длиной массива
    jnz loop                                          ; Если не конец массива, возврат к циклу

skip:
    test eax, 0                                        ; Проверка младшего бита
    jz skip_zero                                      ; Если ноль, переход к следующему байту
    inc ecx                                          ; Увеличение счетчика нулей

skip_zero:
    inc edx                                          ; Увеличение индекса
    cmp edx, len                                      ; Сравнение с длиной массива
    jnz loop                                          ; Если не конец массива, возврат к циклу

    ; Вывод результата
    mov eax, ecx                                      ; Загрузка счетчика нулей в регистр EAX
    mov ebx, 1                                        ; Номер файлового дескриптора stdout
    mov ecx, eax                                      ; Аргумент функции write
    mov edx, 0                                        ; Адрес строки для вывода
    int 0x80                                           ; Системный вызов write

    ; Завершение программы
    mov eax, 1                                        ; Код возврата
    xor ebx, ebx                                      ; Неиспользуемый аргумент
    int 0x80                                           ; Системный вызов exit
