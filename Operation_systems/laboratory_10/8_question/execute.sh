#!/bin/bash

# Начало отчета
echo "Отчет о системе"
echo "====================="
echo ""

# Параметры конфигурации ядра
echo "1. Параметры конфигурации ядра:"
if [ -f /boot/config-$(uname -r) ]; then
    echo "Конфигурация ядра:"
    cat /boot/config-$(uname -r)
else
    echo "Файл конфигурации ядра не найден."
fi
echo ""

# Информация о процессоре
echo "2. Информация о процессоре:"
lscpu
echo ""

# Информация о памяти
echo "3. Информация о памяти:"
free -h
echo ""

# Информация о жестких дисках и SSD
echo "4. Информация о жестких дисках и SSD:"
lsblk
echo ""

# Информация о графической карте
echo "5. Информация о графической карте:"
lspci | grep -i vga
echo ""

# Информация о сетевых адаптерах
echo "6. Информация о сетевых адаптерах:"
lspci | grep -i net
echo ""

# Информация о USB-устройствах
echo "7. Информация о USB-устройствах:"
lsusb
echo ""

# Информация о системе
echo "8. Общая информация о системе:"
uname -a
echo ""

# Информация о модулях ядра
echo "9. Загруженные модули ядра:"
lsmod
echo ""

# Завершение отчета
echo "Отчет завершен."
